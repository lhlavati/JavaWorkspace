package edunova;

import java.util.Date;

public class Vozi {

	private int sifra;
	private Date vrijemePocetka;
	private Date vrijemeKraja;
	private int vozac;
	private int vozilo;
	
	public int getSifra() {
		return sifra;
	}
	public void setSifra(int sifra) {
		this.sifra = sifra;
	}
	public Date getVrijemePocetka() {
		return vrijemePocetka;
	}
	public void setVrijemePocetka(Date vrijemePocetka) {
		this.vrijemePocetka = vrijemePocetka;
	}
	public Date getVrijemeKraja() {
		return vrijemeKraja;
	}
	public void setVrijemeKraja(Date vrijemeKraja) {
		this.vrijemeKraja = vrijemeKraja;
	}
	public int getVozac() {
		return vozac;
	}
	public void setVozac(int vozac) {
		this.vozac = vozac;
	}
	public int getVozilo() {
		return vozilo;
	}
	public void setVozilo(int vozilo) {
		this.vozilo = vozilo;
	}
	
	
}
